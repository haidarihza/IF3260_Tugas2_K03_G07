function setGeometry(gl) {
  const a = 75.0;
  const b = 125.0;
  const c = 25.0;
  const d = a + b;
  const e = d - c;
  const f = 15.0;
  const g = 20.0;
  const m = c + f + g;
  const j = 2*m;
  const i = j - c;
  const l = c + f;
  const h = l + 2*g;
  const k = l - 15.0;
  const n = h + 15.0;
  const o = 150.0;

  const s1 = (x) => {
    return 3*x - 285;
  }

  const s2 = (x) => {
    return -2.08333*x + 325;
  }

  const s3 = (x) => {
    return 2.08333*x + 75;
  }

  const s4 = (x) => {
    return -3*x + 75;
  }

  gl.bufferData(
    gl.ARRAY_BUFFER,
    new Float32Array([
      // front prism
      0,a,0, m,d,0, c,a,0, m,d,0, m,e,0, c,a,0,
      0,a,0, c,a,0, k,0,0, c,a,0, k,0,0, l,c,0,
      k,0,0, l,c,0, h,c,0, k,0,0, h,c,0, n,0,0,
      n,0,0, h,c,0, i,a,0, n,0,0, i,a,0, j,a,0,
      i,a,0, m,e,0, m,d,0, i,a,0, m,d,0, j,a,0,

      // back prism
      0,a,o, m,d,o, c,a,o, m,d,o, m,e,o, c,a,o,
      0,a,o, c,a,o, k,0,o, c,a,o, k,0,o, l,c,o,
      k,0,o, l,c,o, h,c,o, k,0,o, h,c,o, n,0,o,
      n,0,o, h,c,o, i,a,o, n,0,o, i,a,o, j,a,o,
      i,a,o, m,e,o, m,d,o, i,a,o, m,d,o, j,a,o,

      // outer border 1
      k,0,0, n,0,0, k,0,c, n,0,0, k,0,c, n,0,c,
      k,0,o, n,0,o, k,0,o-c, n,0,o, k,0,o-c, n,0,o-c,
      k,0,o-c, k,0,c, k+c,0,o-c, k,0,c, k+c,0,o-c, k+c,0,c,
      n,0,o-c, n,0,c, n-c,0,o-c, n,0,c, n-c,0,o-c, n-c,0,c,

      // outer border 2
      n,0,0, j,a,0, n,0,c, j,a,0, n,0,c, j,a,c,
      n,0,o, j,a,o, n,0,o-c, j,a,o, n,0,o-c, j,a,o-c,
      n,0,c, n+5,s1(n+5),c, n,0,o-c, n+5,s1(n+5),c, n,0,o-c, n+5,s1(n+5),o-c,
      j,a,c, j-5,s1(j-5),c, j,a,o-c, j-5,s1(j-5),c, j,a,o-c, j-5,s1(j-5),o-c,
      
      // outer border 3
      j,a,0, m,d,0, j,a,c, m,d,0, j,a,c, m,d,c,
      j,a,o, m,d,o, j,a,o-c, m,d,o, j,a,o-c, m,d,o-c,
      j,a,c, j-5,s2(j-5),c, j,a,o-c, j-5,s2(j-5),c, j,a,o-c, j-5,s2(j-5),o-c,
      m,d,c, m+5,s2(m+5),c, m,d,o-c, m+5,s2(m+5),c, m,d,o-c, m+5,s2(m+5),o-c,

      // outer border 4
      m,d,0, 0,a,0, m,d,c, 0,a,0, m,d,c, 0,a,c,
      m,d,o, 0,a,o, m,d,o-c, 0,a,o, m,d,o-c, 0,a,o-c,
      m,d,c, m-5,s3(m-5),c, m,d,o-c, m-5,s3(m-5),c, m,d,o-c, m-5,s3(m-5),o-c,
      0,a,c, 0+5,s3(0+5),c, 0,a,o-c, 0+5,s3(0+5),c, 0,a,o-c, 0+5,s3(0+5),o-c,

      // outer border 5
      0,a,0, k,0,0, 0,a,c, k,0,0, 0,a,c, k,0,c,
      0,a,o, k,0,o, 0,a,o-c, k,0,o, 0,a,o-c, k,0,o-c,
      0,a,c, 0+5,s4(0+5),c, 0,a,o-c, 0+5,s4(0+5),c, 0,a,o-c, 0+5,s4(0+5),o-c,
      k,0,c, k-5,s4(k-5),c, k,0,o-c, k-5,s4(k-5),c, k,0,o-c, k-5,s4(k-5),o-c,

      // // inner border 1
      // l,c,0, h,c,0, l,c,o, h,c,0, l,c,o, h,c,o,

      // // inner border 2
      // h,c,0, i,a,0, h,c,o, i,a,0, h,c,o, i,a,o,

      // // inner border 3
      // i,a,0, m,e,0, i,a,o, m,e,0, i,a,o, m,e,o,

      // // inner border 4
      // m,e,0, c,a,0, m,e,o, c,a,0, m,e,o, c,a,o,

      // // inner border 5
      // c,a,0, l,c,0, c,a,o, l,c,0, c,a,o, l,c,o,
    ]),
    gl.STATIC_DRAW
  );
}

